		<img src="images/history/snitch.jpg" id="snitch"/>
		<p>For the next eight months, I devoted every minute of my spare time to designing the cards and perfecting the rules. Over and over again I would ask different families if they would like to try out a new game. I would go to their house, hand them the game and sit on the sidelines with my notebook and observe them reading the rules, figuring out how to play, and playing the game.</p>
		<p>It was through this tedious process of testing and refining that the original game (now called the "Classic Edition") was created.</p>
		<p>Our first year of sales was absolutely amazing. We sold over 5,000 games which is almost unheard of in the board game industry. We knew we had something great.</p>
		<p>The next year we spent our time working on distribution across the country. Try as we might, we could not get into any major retail chains, but thanks to a solid base of independent toy, book, and variety stores we had another great year almost tripling our sales from the first year.</p>
		<p>In 2011 we also brought Snitch the Fraccoon to life. We hired local artist Larry Frates (who had drawn the original sketches of Snitch) to build a life-sized head so we could have Snitch show up at stores and be in parades. My wife Linda designed the suit and Snitch was born.</p>
		<p>2012 was our expansion year. We finalized the design for the Junior Edition of the game which would allow players as young as six years old to get in on the fun. Because Snitch was so very popular everywhere he showed up, we decided to create a plush toy for kids to have and hold. Finally, we wrote the first book about Snitch which was published in November.</p>

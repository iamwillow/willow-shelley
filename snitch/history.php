	<img src="images/history/card.gif" id="card"/>
		<p>Bag-O-Loot was first invented several years ago when my daughter and I were stuck in a ski lodge one night looking for something to do. We looked around and just yawned at the idea of playing the same old games, so we grabbed a couple decks of cards and decided to invent our own game.</p>
		<p>At that time, we came up with the basic concept: Players would try and make sets of matching cards and other players could steal them from you if they had a matching card.</p>
		<p>After a bit we added the bluffing/challenging part of the game, and that is when it got really fun. We decided that when completing a set, the last card would go face down, and if you wanted to, you could play a non-matching card and try to get away with it. Bag-O-Loot was born!</p>
		<p>I wrote down the rules and brought them home after that weekend. For the next couple of weeks, the rules were tweaked as I tried to work out how to keep the game interesting with an nice balance of both strategy and luck.</p>
		<p>We would play with friends that would come over to get some feedback, and I even asked my sister to try and design some cards. In fact, she was the one that first came up with the idea of a little raccoon on all the cards who eventually evolved into Snitch the Fraccoon. Then life got busy and the game sat on a shelf and became a "someday" project.</p>
		<p>Finally, in 2009, "someday" came. I brought it back out - this time with the full intention of producing and selling the game.</p>
